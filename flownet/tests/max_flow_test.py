"""This module contains the general flownet tests.

"""
from unittest import TestCase, TestSuite, TextTestRunner
from flownet import FlowNetwork


def get_max_flow(adj_matrix, sources, sinks):
    """Gets the maximum flow of the flow network from sources to sinks.

    Args:
            adjacency_matrix (list[list[int]): The adjacency matrix representation
                of a flow graph.
            sources (list[int]): list of integers denoting indexes of sources.
            sinks (list[int]): list of integers denoting indexes of sinks.

    Returns:
        int: The maximum flow.

    """
    flow_network = FlowNetwork.from_adjacency_matrix(adj_matrix, sources, sinks)
    return flow_network.get_maximum_flow()


class MaxFlowTestCases(TestCase):
    def test_max1(self):
        sources = [0]
        sinks = [1]
        adj_matrix = [
            [0, 0],
            [0, 0]
            ]
        assert get_max_flow(adj_matrix, sources, sinks) == 0


    def test_max2(self):
        sources = [0]
        sinks = [1]
        adj_matrix = [
            [2, 6],
            [3, 1]
            ]
        assert get_max_flow(adj_matrix, sources, sinks) == 6

